class CharEx2 {

	public static void main(String[] args) {
		
		int ch;

		
		ch = 65;

		System.out.println("the value of ch is " + ch);

		System.out.println("the value oc ch is " + (char)ch);

		++ch;

		System.out.println("the value of ch is " + ch);

		System.out.println("the value of ch is " + (char)ch);

		--ch;

		System.out.println("the value of ch is " + ch);

		System.out.println("the value of ch is "+ (char)ch);
	}
}