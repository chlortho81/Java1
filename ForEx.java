// call this program ForEx.java

class ForEx {

public static void main(String args[]) {

double i;

for (i=0.0; i < 5.0;i += .5)
        System.out.println(i);

System.out.println("now let's print it backwards ");

for (i=5.0;i >= -5.0;i -= .5)
        System.out.println(i);

}//main

}//ForEx

